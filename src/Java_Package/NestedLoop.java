package Java_Package;

public class NestedLoop {
	
	public static void main(String[] args)
	{
		
		for (int i=1; i<2; i++)
		
		{
		
	
			for (int j= 1; j<5; j++)
				
			{	
				System.out.print(j);
				System.out.print("\t");
				
			
			
			
		}
			
			for (int k=5; k<6; k++)
				
			{
				System.out.println("");
				
				
			}
				for (int m= 5; m<8; m++)
					
				{	
					System.out.print(m);
					System.out.print("\t");
				
				
				
			}
				
				for (int r=8; r<9; r++)
					
				{
					System.out.println("");
					
				}
					for (int f= 8; f<10; f++)
						
					{	
						System.out.print(f);
						System.out.print("\t");
					
					
					
				}
		
					for (int g=10; g<11; g++)
						
					{
						System.out.println("");
						
						System.out.print(g);
					}
						
						
						
						
					
	}
	}
}

