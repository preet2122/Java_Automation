package Java_Package;

public class Subtract {
	
	static int sub (int a, int b)
	{
	int c = a-b; 
	return c;
	}
	
	public static void main(String[] args)
	{
	int n1 = 225;
	int n2 = 115;
	int m;
	m = sub (n1, n2);
	System.out.println("The sub of numbers is: "+m);
}
}



